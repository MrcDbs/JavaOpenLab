package it.tris.scacchiera.dto;

public class MossaDTO {
	private Integer idUtente;
	private Integer idPartita;
	private Integer orizzontalPos;
	private Integer verticalPos;
	
	
	public Integer getIdUtente() {
		return idUtente;
	}
	public void setIdUtente(Integer idUtente) {
		this.idUtente = idUtente;
	}
	public Integer getIdPartita() {
		return idPartita;
	}
	public void setIdPartita(Integer idPartita) {
		this.idPartita = idPartita;
	}
	public Integer getOrizzontalPos() {
		return orizzontalPos;
	}
	public void setOrizzontalPos(Integer orizzontalPos) {
		this.orizzontalPos = orizzontalPos;
	}
	public Integer getVerticalPos() {
		return verticalPos;
	}
	public void setVerticalPos(Integer verticalPos) {
		this.verticalPos = verticalPos;
	}
	
	

}
